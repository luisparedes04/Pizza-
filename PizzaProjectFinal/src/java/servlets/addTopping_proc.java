    /**
     * Processes requests for both HTTP <code>GET</code> and <code>POST</code>
     * methods.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    protected void processRequest(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException, Exception {
        response.setContentType("text/html;charset=UTF-8");
        try (PrintWriter out = response.getWriter()) {
            /* TODO output your page here. You may use following sample code. */
            out.println("<!DOCTYPE html>");
            out.println("<html>");
            out.println("<head>");
            out.println("<title>Servlet addTopping_proc</title>");            
            out.println("</head>");
            out.println("<body>");
            String name = request.getParameter("topping");
            String price = request.getParameter("price");
            String empID = request.getParameter("emp");
            
            out.println(name);
            out.println(price);
            out.println(empID);
            
            
            boolean add = ToppingDL.InsertTopping(name, Double.parseDouble(price), Integer.parseInt(empID));
            
            if(add){
                response.sendRedirect("EmpManager.jsp?msgAdd=Topping Added");            
            }else{
                response.sendRedirect("EmpManager.jsp?msgAdd=Topping Could not be add. Contact manager or topping already exists");
            }
            out.println("<h1>Servlet addTopping_proc at " + request.getContextPath() + "</h1>");
            out.println("</body>");
            out.println("</html>");
        }
    }

